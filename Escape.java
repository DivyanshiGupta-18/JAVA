public class Escape {
    public static void main (String[] args){
        System.out.println("Hello\"KGCoding\"...");
        System.out.println("Hello\\KGCoding\\..."); /*insert a backslash character in the text at this point*/
        System.out.println("Hello\tKGCoding..."); /*insert a tab in the text at this point*/
        System.out.println("Hello\nKGCoding..."); /*insert a newline in the text at this point*/
        System.out.println("Hello\'KGCoding'..."); /*insert a single quote in the text at this point*/
        System.out.println("Hello\bKGCoding..."); /*insert a backspace in the text at this point*/
        System.out.println(" *\n ** \n *** \n **** \n *****");
    }
}